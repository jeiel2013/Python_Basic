# abs() -> retornar o valor absoluto do número
print(abs(-9))
# min() -> retorna o valor minimo de uma sequencia
print(min(7,6,4,8,9,10))
# max() -> retorna o valor máximo de uma sequência
print(max(7,6,4,8,9,10))
# sum() -> faz a soma dos valores de uma sequencia
print(sum([7,6,4,8,9,10]))
# pow() -> exponenciação
# 2**3
print(pow(2,3))
# print() -> mostra um valor na saída
print('Hello World!')
# input() -> lê um valor digitado no teclado pelo usuário
nome = input('Qual seu nome? \n')
print(nome)
# help() -> mostra as funcionalidades de uma determinada função
help(max)