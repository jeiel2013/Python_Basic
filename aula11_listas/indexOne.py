mercado = ['cebola', 'alho', 'macarrão']
mercado.append('molho') # acrescenta um elemento no final da lista
print(mercado)

print(mercado.index('macarrão')) # retorna o índice de um determinado elemento

mercado.insert(1, 'refrigerante') # vai acrescentar no índice que colocarmos o valor
print(mercado)

mercado.sort()
print(mercado)

mercado.reverse()
print(mercado)